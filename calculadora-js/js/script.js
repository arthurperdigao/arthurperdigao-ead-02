function calculadora() {
	var valor1 = parseInt(prompt('Informe 1º valor: '));
	var valor2 = parseInt(prompt('Informe 2º valor: '));
	
		alert('A + B= ' + (valor1 + valor2) + "\n"+
			  'A - B= ' + (valor1 - valor2) + "\n"+
			  'A * B= ' + (valor1 * valor2) + "\n"+
			  'A / B= ' + (valor1 / valor2) + "\n"+
			  'A % B= ' + (valor1 % valor2) + "\n");
	
}




